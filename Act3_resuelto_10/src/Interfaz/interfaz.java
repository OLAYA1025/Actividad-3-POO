    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        long patrimonio=(long)this.spnPatrimonio.getValue();
        int estrato=(int)this.SpnEstrato.getValue();
        String nombre=this.txtname.getText();
        String NumeroInscripcion=this.txtNum.getText();
        
        Estudiante estudiante=new Estudiante(NumeroInscripcion, nombre, patrimonio, estrato);
        double valor_matricula=estudiante.CalcularMatricula();
        
        JOptionPane.showMessageDialog(this,"Numero de inscripci√≥n: "+this.txtNum.getText()+"\nNombre: "
                +this.txtname.getText()+"\nPago de matricula: "+valor_matricula, "Pago de matricula",JOptionPane.INFORMATION_MESSAGE);
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

